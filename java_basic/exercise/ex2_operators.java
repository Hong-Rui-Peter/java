package exercise;

//運算子
public class ex2_operators {
    static int a = 3;

    public static void run() {
        System.out.println("ex2_operators:");
        System.out.println("--:" + --a);
        System.out.println("+=:" + (a += 3));
        System.out.println("++:" + ++a);
        System.out.println("-=:" + (a -= 5));
        System.out.println();
    }
}
